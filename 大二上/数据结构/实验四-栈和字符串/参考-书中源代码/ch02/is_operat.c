/*  函数功能：判断一个字符是否为运算符                 */
/*  函数参数：char类型变量op                           */
/*  函数返回值：int类型。返回1表示op运算符，否则不是。*/
/*  文件名：is_operat.c，函数名：is_operation()              */
/********************************************************/
int is_operation(char op)
 {
   switch(op)
   { case '+':
     case '-':
     case '*':
     case '/':return 1;
     default:return 0;
    }
 }

